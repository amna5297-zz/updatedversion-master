            From what started off as a class project undertaken by the students of NUST Business
            School in 2010, to the country’s biggest Model United Nations conference in the years that
            followed, NUST International Model United Nations has come a long way.<br /><br />

            Starting in 2012, when it only existed as NUST Model United Nations, under the motto
            <b>“Listen, Speak, Resolve”</b>, a precedent was set, a four day Model United Nations conference
            so exemplary, it would create a benchmark for exceptional debate and enthralling social
            events, a conference unlike anything in Pakistan in terms of the exposure it provides.<br /><br />

            After its humble beginnings in 2012, a year later, NUST Model United Nations became the
            first conference in Pakistan to host international chairs and delegates, thus giving birth to
            NUST International Model United Nations or NIMUN 2013. The broadening of the NIMUN
            Directorate by the addition of an “International Linkages” portfolio, an initiative taken and
            successfully executed a mere month before the conference, was NIMUN’s first step into
            expanding its horizons and stretching its boundaries to allow growth in terms of what MUN
            conferences could mean in Pakistan. Perhaps, one of NIMUN’s fondest memories is of
            receiving its first ever international delegates from Nepal and China. Under the motto <b>“Let
            Diplomacy Be Your Argument”</b>, NIMUN 13 had proved itself as the leading MUN conference
            in the country.<br /><br />

            NIMUN 2014 showed more promising results with a count of over 600 delegates from all
            around the world, including participants from countries such as Nepal, Saudi Arabia and
            Germany. With the theme <b>“MUNinted for a better tomorrow”</b>, the emphasis to the
            execution was individual ownership – from the Organizing Committee to the faculty
            advisors, and inevitably the Secretariat itself, everyone worked for more than what they
            were required to, and took the initiative for ownership of the conference, which contributed
            the most to the success of the conference. The delegates were mesmerized with the
            invigorating debate that had improved substantially over its precedent conference, and an
            array of spectacular social events that imprinted the conference in the minds and hearts of
            many.<br /><br />

            The stage was set; the preparations were done. NIMUN was geared up for an event of even
            higher calibre, when tragedy struck the country in form of the December 16th attack on the
            Army Public School in Peshawar. Staying true to our ideology of nurturing unity and
            brotherhood, it was decided that the sorrow and heartache that spread in the society would
            be dealt with in a manner which would be in accordance with the national sentiment:
            NIMUN’15 was unplugged with the resolve of coming back bigger, better and stronger next
            time.<br /><br />

            Owing to a year of inactivity, it became more of a challenge to successfully execute and
            meet benchmarks set by the 2014 conference. In 2016, the NIMUN Core Committee,
            complemented with a very hardworking and dedicated Organizing Committee and the best
            Secretariat members in the country, achieved all its goals and became the best
            international-level conference in Pakistan, to date. Embracing the theme <b>“Unity Beyond
            Borders”</b>, the conference delivered what it promised, with a delegate count of more than
            600 participants attending the conference from around the world, including from countries
            such as Japan, Saudi Arabia, Germany and Switzerland. If the heated debate didn’t define
            the high standards that NIMUN strives to achieve, the social events served as a cherry on
            the top of the already magnificent event, with the melody of happiness echoing to the
            hearts.



