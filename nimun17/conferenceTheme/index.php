            In keeping with the tradition of addressing the most important issues of today, the theme for
            NIMUN ’17 will emphasize <b>“Together towards Tomorrow”</b>, an initiative for collaboration
            across borders for a sustainable future based on 7 of the 17 Sustainable Development Goals
            (SDGs) which comprise UN’s 2030 Agenda for Sustainable Development. The debate this
            year is larger than ever, both in impact and scale, as current scenarios and future projections
            are both taken into account and issues ranging from battling inequality to taking active
            responsibility for our home, Earth, will be raised and resolved.
            <br /><br /><br />
            The sustainability goals chosen are all based on the imperatives for environmentally and
            socially sensitive development, understanding the need for formulating policies to reverse
            the damage already done, prevent further damage and reinforce healthy progress. The goals
            further highlight the absolute necessity of joining hands and shared resources for a better,
            sustainable tomorrow.
            <br /><br /><br />
            The SDGs on agenda, as described by the UN, are:
            <br />
            <br /><b>Goal no. 4 Quality Education:</b> Ensure inclusive and equitable quality education and
            promote lifelong learning opportunities for all,
            <br /><b>Goal no. 6 Clean Water and Sanitation:</b> Ensure availability and sustainable management
            of water and sanitation for all,
            <br /><b>Goal no. 8 Decent Work and Economic Growth:</b> Promote sustained, inclusive and
            sustainable economic growth, full and productive employment and decent work for all,
            <br /><b>Goal no. 10 Reduced Inequalities:</b> Reduce inequality within and among countries,
            <br /><b>Goal no. 12 Responsible Consumption and Production:</b> Ensure sustainable consumption
            and production patterns,
            <br /><b>Goal no. 16 Peace, Justice and Strong Institutions:</b> Promote peaceful and inclusive
            societies for sustainable development, provide access to justice for all and build effective,
            accountable and inclusive institutions at all levels,  
            <br /><b>Goal no. 17 Partnership for the Goals:</b> Strengthen the means of implementation and
            revitalize the Global Partnership for Sustainable Development.

