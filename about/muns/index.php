						<span class="lato size22 dark bold">History</span><br/>
						<div class="line4"></div>
            Model United Nations (abbreviation: Model UN, MUN) is an academic simulation of the United Nations that aims to instil a sense of diplomacy and dialogue among its participants, or delegates as they are referred to in the Model UN world. Model UN began as a series of student-led Model ‘League of Nations’ simulations, before transitioning to the Model ‘United Nations’ simulations after the formation of the League's successor organization, the United Nations, in 1945. Model United Nations hold a history far bigger than any other event in place. With various renowned conferences such as those simulated by Harvard and UC Berkeley, it has marked its place as the most preferred extracurricular activity by students worldwide.

            <br /><br /><br />
            <span class="lato size22 dark bold">How does a Model UN function?</span><br/>
            <div class="line4"></div>
            Participants assume the ambassadorial roles of their respective allotted countries as foreign diplomats, participating in a simulated session of an intergovernmental organization (IGO), mostly mirroring actual UN bodies. At many Model UN conferences, however, the committees are not only limited to the latter – specialized bodies, mostly representing the local country’s parliamentary body, are also part of the committee portfolio. Each committee holds a set of various sessions, during which participants step into the shoes of diplomats to discuss and debate on pertinent issues on the organization's vast agenda. Delegates are mostly required to prepare draft resolutions, plot a diplomatic strategy, effectively negotiate with supporters and adversaries, resolve conflicts that may arise, and intensely navigate the UN's rules of procedures, as applied in the respective UN bodies, all in the interest of mobilizing "international cooperation" to resolve problems that affect almost every country on Earth. All delegates have to obey these Rules of Procedure, which are a simplified version of those used in the real UN conferences. Simplified as such, delegates are expected to investigate international issues, debate, deliberate, consult, and then develop solutions to issues around the world.

            <br /><br /><br />
            <span class="lato size22 dark bold">What does a Model UN deliver?</span>
            <div class="line4"></div>
            The primary objective of organizing Model UN conferences is to educate participants about civics, effective communication, globalization and multilateral diplomacy. Many factors mark Model UNs as fruitful and experientially productive for its delegates, but more specifically, it is the educational benefits that contribute the most in making it count. For starters, such conferences motivate a person to learn more. It is fun to pretend being a world leader solving the world’s most important problems in 48 hours or less. Model UN activates participants’ imagination and creativity – activities that participants are naturally inclined to do. If that wasn’t reason enough, it is the fact that Model UNs complement the academic knowledge and combine it with real-world skills like critical analysis, crisis resolution, and diplomatic negotiation. Model UNs also help in the broadening of a participant’s viewpoint regarding global issues due to the representation of differing perspectives by the delegates of other countries.<br />
            It’s not all work! While the focus is on the benefits of the simulations themselves, the conference comes with perquisites such as the social events and the in-committee entertainment sessions, that brighten up the ambiance of the committee after a heated session. Participants get a chance to have fun, and that is as much a part of the MODEL UN experience as the committee sessions.
